public class Job {
        //Attributes
    private String role;
    private long salary;
    private int id;
    
    //Get
    public String getRole(){
        return role;
    }
    public long getSalary(){
        return salary;
    }
    public int getID(){
        return id;
    }
    
//set
    public void setRole(String role){
        this.role = role ;
    }
    public void setSalary(long salary){
        this.salary= salary;
    }
    public void setID(int id){
        this.id = id ;
    }
    
    
    
    
}
